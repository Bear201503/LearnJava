package datatypes;

public class PrimitaveDataTypes {
    public static void main(String[] args) {


        boolean flag = false;

        System.out.println("flag value is " + flag);

        flag = true;

        System.out.println("New flag value is " + flag);

        char ch = 'c';

        System.out.println("Value of character is " + ch);

        char es = 'd';

        System.out.println("New value of character is " + es);

        int i = 8;

        System.out.println("Value of i is " + i);

         i = 10;

        System.out.println("New value of i is " + i);

        long l = 9l;

        System.out.println("Value of long is " + l);

        float f = 1.2f;

        System.out.println("The Value of f is: " + f);

        double d = 1.25787856d;

        System.out.println("The value of d is: " + d);


    }
}
